    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        try {
            JasperDesign jasperdesign=JRXmlLoader.load("F:\\DSE224\\EAD\\Activity\\CourseWork\\Coursework\\src\\Donation.jrxml");
            JRDesignQuery designquery=new JRDesignQuery();
            designquery.setText("select  a.a_id, a.user_id,a.center AS appointment_center,a.date AS appointment_date,a.blood_type AS donated_blood_type FROM appointment a JOIN donation d ON a.a_id = d.a_id");
            jasperdesign.setQuery(designquery);
            JasperReport report=JasperCompileManager.compileReport(jasperdesign);
            JasperPrint print=JasperFillManager.fillReport(report,null,DBConnection.getMyConnection());
            JasperViewer.viewReport(print);  
        } 
        catch (JRException ex) {
            Logger.getLogger(ADonation.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
       
    }//GEN-LAST:event_btnReportActionPerformed

